        var vx = new Vue({
            el: '#app',

            data: {
                // expenses: {}
                expenses: [],
                loaded: 1

            },

            created: function(){

                this.getExpenses();
                this.loaded = 0;
            },

            methods: {

                getExpenses()
                {
                    $.getJSON("{{ route('all_expense') }}", function(expenses){
                        this.expenses = expenses;
                    }.bind(this));
                },

                getFormValues()
                {
                    
                    var expense_value = this.$refs.value_input.value;
                    expense_value = expense_value.replace ( /[^\d.]/g, '' );

                    $.ajax({
                        url: "https://free.currencyconverterapi.com/api/v5/convert?q=EUR_GBP&&compact=ultra",
                        type: "GET",

                        beforeSend: function(){
                            $('#payment-button-amount-2').text("Saving...");
                        },


                        complete: function(){
                            // $('#chat-loader').hide();
                        },

                        success: function(data){
                           
                            var vat_value = 0.2 * expense_value; // 20% VAT value
                            var new_expense_value = 0;

                            
                            new_expense_value = data.EUR_GBP * expense_value;
                            // console.log(new_expense_value);
                            // console.log("here");

                            vx.expenses.push({
                                date: vx.$refs.date_input.value,
                                value: new_expense_value,
                                reason: vx.$refs.reason_input.value,
                                vat: vat_value
                            });

                            $('#payment-button-amount-2').text("Completed !");

                            $("#smallmodal").modal('hide');
                        },

                        error: function(data){
                            $('#payment-button-amount-2').text("An error occured!");
                        }
                        });

                   
                },

                saveToDatabase(expense)
                {

                    $.ajaxSetup({
                        global: false,

                        headers: {
                            'X-CSRF-TOKEN' : $('meta[name="csrf-token"]').attr('content')
                        }

                    });

                    $.ajax({

                    type: "post",
                    url: "/expense",
                    data: {
                        date: expense.date,
                        value: expense.value,
                        reason: expense.reason,
                        vat: expense.vat
                    },

                    beforeSend: function(){
                        $('#save-button').text("Saving...");
                    },

